                <p class="lead desc">Menit.com adalah salah satu pionir media online di Indonesia ketika pertama kali hadir di Internet pada 14 September 1995 dengan nama Menit Online. 
                                     Mulanya, Menit Online atau KOL yang diakses dengan alamat Menit.co.id hanya menampilkan replika dari berita-berita harian Menit yang terbit hari itu. 
                                     Tujuannya adalah memberikan layanan kepada para pembaca harian Menit di tempat-tempat yang sulit dijangkau oleh jaringan distribusi Menit. Dengan hadirnya 
                                     Menit Online, para pembaca harian Menit terutama di Indonesia bagian timur dan di luar negeri dapat menikmati harian Menit hari itu juga, tidak perlu menunggu beberapa hari seperti biasanya.
                                     Selanjutnya, demi memberikan layanan yang maksimal, di awal tahun 1996 alamat Menit Online berubah menjadi www.Menit.com. Dengan alamat baru, 
                                     Menit Online menjadi semakin populer buat para pembaca setia harian Menit di luar negeri.
                                     Melihat potensi dunia digital yang besar, Menit Online kemudian dikembangkan menjadi sebuah unit bisnis tersendiri di bawah bendera PT Menit Cyber Media (KCM) pada 6 Agustus 1998. 
                                     Sejak saat itu, Menit Online lebih dikenal dengan sebutan KCM. Di era ini, para pengunjung KCM tidak lagi hanya mendapatkan replika harian Menit, tapi juga mendapatkan update perkembangan berita-berita terbaru yang terjadi sepanjang hari.
                                     Pengunjung KCM meningkat pesat seiring dengan tumbuhnya pengguna Internet di Indonesia. Mengakses informasi dari Internet kini telah menjadi bagian tak terpisahkan dari hidup kita sehari-hari. Dunia digital pun terus berubah dari waktu ke waktu. 
                                     KCM pun berbenah diri. 
                                     Pada 29 Mei 2008, portal berita ini me-rebranding dirinya menjadi Menit.com, merujuk kembali pada brand Menit yang selama ini dikenal selalu menghadirkan jurnalisme yang memberi makna. Kanal-kanal berita ditambah. 
                                     Produktivitas sajian berita ditingkatkan demi memberikan sajian informasi yang update dan aktual kepada para pembaca. Rebranding Menit.com ingin menegaskan bahwa portal berita ini ingin hadir di tengah pembaca sebagai acuan bagi jurnalisme yang baik di tengah derasnya aliran informasi yang tak jelas kebenarannya.</p>
